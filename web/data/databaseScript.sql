CREATE TABLE Users (
	userID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	fName VARCHAR(30) NOT NULL,
	lName VARCHAR(30) NOT NULL,
	email VARCHAR(50) NOT NULL UNIQUE,
	passwrd VARCHAR(50) NOT NULL,
	artist BOOLEAN NOT NULL,
	description VARCHAR(350),
	categories VARCHAR(50),
	subcategories VARCHAR(50)
);

CREATE TABLE Inbox (
	inboxID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	user1ID INT NOT NULL,
	user2ID INT NOT NULL,
	FOREIGN KEY (user1ID) REFERENCES Users(userID),
	FOREIGN KEY (user2ID) REFERENCES Users(userID)
);

CREATE TABLE Messages(
	messageID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	inboxID INT NOT NULL,
	userID INT NOT NULL,
	msgTime INT NOT NULL,
	message VARCHAR(300) NOT NULL,
	FOREIGN KEY (inboxID) REFERENCES Inbox(inboxID),
	FOREIGN KEY (userID) REFERENCES Users(userID)
);

CREATE TABLE CommissionDetails (
	commissionID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	userID INT NOT NULL,
	type VARCHAR(50) NOT NULL,
	description VARCHAR(350) NOT NULL,
	details VARCHAR(350) NOT NULL,
	FOREIGN KEY (userID) REFERENCES Users(userID)
);